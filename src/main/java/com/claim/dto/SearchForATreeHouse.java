package com.claim.dto;

public class SearchForATreeHouse {
	
	//Attributes
	String treeHouseName;
	String treeMemberFirstName;
	String treeMemberLastName;
	String treeMemberEmail;
	
	
	//Constructor
	public SearchForATreeHouse() {}

	
	//Getters and Setters
	public String getTreeHouseName() {
		return treeHouseName;
	}


	public void setTreeHouseName(String treeHouseName) {
		this.treeHouseName = treeHouseName;
	}


	public String getTreeMemberFirstName() {
		return treeMemberFirstName;
	}


	public void setTreeMemberFirstName(String treeMemberFirstName) {
		this.treeMemberFirstName = treeMemberFirstName;
	}


	public String getTreeMemberLastName() {
		return treeMemberLastName;
	}


	public void setTreeMemberLastName(String treeMemberLastName) {
		this.treeMemberLastName = treeMemberLastName;
	}


	public String getTreeMemberEmail() {
		return treeMemberEmail;
	}


	public void setTreeMemberEmail(String treeMemberEmail) {
		this.treeMemberEmail = treeMemberEmail;
	}
		
}
